/**
 * Representa os tipos de avaliação que um usuário pode fazer em um artigo.
 */
public enum TipoAvaliacao {
    // Usuário gostou do conteúdo do artigo
    GOSTEI,
    // Usuário ficou neutro em relação ao artigo
    NEUTRO,
    // Usuário não gostou do artigo
    NAO_GOSTEI
}
